const drawPath = (ctx, points, closePath) => {
  //  skip if ANY point is undefined or not an array
  if (!points || points.some((p) => !p || !Array.isArray(p))) return;

  const region = new Path2D();
  region.moveTo(points[0][0], points[0][1]);
  for (let i = 1; i < points.length; i++) {
    region.lineTo(points[i][0], points[i][1]);
  }
  if (closePath) region.closePath();
  ctx.strokeStyle = "pink";
  ctx.stroke(region);
};

export const drawMesh = (predictions, ctx) => {
  if (!predictions || predictions.length === 0) return;

  predictions.forEach((prediction) => {
    const keypoints = prediction.scaledMesh;
    if (!keypoints || keypoints.length === 0) return;

    ctx.clearRect(0, 0, ctx.canvas.width, ctx.canvas.height);

    // Draw triangles
    for (let i = 0; i < TRIANGULATION.length / 3; i++) {
      const indices = [
        TRIANGULATION[i * 3],
        TRIANGULATION[i * 3 + 1],
        TRIANGULATION[i * 3 + 2],
      ];

      //  skip if any index is out of bounds
      if (indices.some((idx) => idx === undefined || idx >= keypoints.length))
        continue;

      const points = indices.map((idx) => keypoints[idx]);
      drawPath(ctx, points, true);
    }

    // Draw dots
    for (let i = 0; i < keypoints.length; i++) {
      const point = keypoints[i];
      if (!point || point[0] === undefined || point[1] === undefined) continue;
      ctx.beginPath();
      ctx.arc(point[0], point[1], 1, 0, 2 * Math.PI);
      ctx.fillStyle = "aqua";
      ctx.fill();
    }
  });
};

// ✅ Eye Aspect Ratio for drowsiness
export const getEAR = (keypoints) => {
  if (!keypoints || keypoints.length === 0) return 1;

  const leftEye = [33, 160, 158, 133, 153, 144];
  const rightEye = [362, 385, 387, 263, 373, 380];

  const eyeAspectRatio = (eye) => {
    const p = eye.map((i) => keypoints[i]);
    if (p.some((pt) => !pt)) return 1;
    const vertical1 = Math.hypot(p[1][0] - p[5][0], p[1][1] - p[5][1]);
    const vertical2 = Math.hypot(p[2][0] - p[4][0], p[2][1] - p[4][1]);
    const horizontal = Math.hypot(p[0][0] - p[3][0], p[0][1] - p[3][1]);
    return (vertical1 + vertical2) / (2.0 * horizontal);
  };

  return (eyeAspectRatio(leftEye) + eyeAspectRatio(rightEye)) / 2;
};

// ✅ Face direction detection
export const getFaceDirection = (keypoints) => {
  if (!keypoints || keypoints.length === 0) return "forward";
  const noseTip = keypoints[1];
  const leftEar = keypoints[234];
  const rightEar = keypoints[454];
  if (!noseTip || !leftEar || !rightEar) return "forward";
  const faceCenter = [
    (leftEar[0] + rightEar[0]) / 2,
    (leftEar[1] + rightEar[1]) / 2,
  ];
  const diff = noseTip[0] - faceCenter[0];
  if (diff < -30) return "right";
  if (diff > 30) return "left";
  return "forward";
};

// ✅ Smile detection
export const isSmiling = (keypoints) => {
  if (!keypoints || keypoints.length === 0) return false;
  const leftMouth = keypoints[61];
  const rightMouth = keypoints[291];
  const topLip = keypoints[13];
  const bottomLip = keypoints[14];
  if (!leftMouth || !rightMouth || !topLip || !bottomLip) return false;
  const mouthWidth = Math.hypot(
    rightMouth[0] - leftMouth[0],
    rightMouth[1] - leftMouth[1],
  );
  const mouthHeight = Math.hypot(
    bottomLip[0] - topLip[0],
    bottomLip[1] - topLip[1],
  );
  return mouthHeight / mouthWidth > 0.3;
};
